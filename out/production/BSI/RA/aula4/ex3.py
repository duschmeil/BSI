pontos = int(input("Quantos pontos de liga você tem? "))
if pontos <= 999:
    print("Seu Rank é: Ferro!")
elif 1000 <= pontos <= 1999:
    print("Seu Rank é: Bronze!")
elif 2000 <= pontos <= 2999:
    print("Seu Rank é: Prata!")
elif 3000 <= pontos <= 3999:
    print("Seu Rank é: Ouro!")
elif 4000 <= pontos <= 4999:
    print("Seu Rank é: Platina!")
elif 5000 <= pontos <= 5999:
    print("Seu Rank é: Diamante!")
elif 6000 <= pontos <= 6999:
    print("Seu Rank é: Mestre!")